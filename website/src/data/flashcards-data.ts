// Helper to get cards by category
export const getFlashCardsByCategory = (categoryId: string | null): FlashCard[] => {
    if (!categoryId || categoryId === 'all') {
        return FLASH_CARDS_DATA;
    }
    return FLASH_CARDS_DATA.filter((card) => card.category === categoryId);
};

// Helper to get cards due for review
export const getDueFlashCards = (
    cards: FlashCard[],
    progressMap: Map<string, { nextReviewDate: string | null }>,
    today: string
): FlashCard[] => {
    return cards.filter((card) => {
        const progress = progressMap.get(card.id);
        if (!progress || !progress.nextReviewDate) return true; // New cards
        return progress.nextReviewDate <= today;
    });
};

// Get unique categories
export const getFlashCardCategories = (): { id: string; name: string }[] => {
    const categories = new Map<string, string>();
    categories.set('all', 'All Cards');

    FLASH_CARDS_DATA.forEach((card) => {
        if (!categories.has(card.category)) {
            // Convert category ID to readable name
            const name = card.category
                .split('-')
                .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
                .join(' ');
            categories.set(card.category, name);
        }
    });

    return Array.from(categories.entries()).map(([id, name]) => ({ id, name }));
};
